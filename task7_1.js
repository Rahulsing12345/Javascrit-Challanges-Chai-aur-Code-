// Create an object representing an book with properties like Title, Author, and Year, and log the object to the console.

const myBookObj = {
    title : "Mathematics",
    author : "RD Sharma",
    Year : "2004"
}
console.log(myBookObj);

// OUTPUT : 
// { title: 'Mathematics', author: 'RD Sharma', Year: '2004' }

const bookObject = {}
bookObject.title = "Science"
bookObject.author = "S Chand"
bookObject.Year = "1958"

console.log(bookObject);

// OUTPUT : 
// { title: 'Science', author: 'S Chand', Year: '1958' }


