// Write a function to check if a number is even or odd and log the result to the console.

function evenOdd(num) {
    if (num % 2 ==0) {
        console.log(`${num} is Even`);
    } else {
        console.log(`${num} is Odd`);
        
    }
}

evenOdd(4)
evenOdd(5)
evenOdd(0)
evenOdd(1)




