// Write an arrow function to check if a string contains a specific character and return a boolean value.

const checkCharInStr = ( str, char ) => {
    return str.includes( char );
}

console.log(checkCharInStr( "Rahul Singh" , "b"));
console.log(checkCharInStr( "Rahul Singh" , "a"));







